#include "ft_printf.h"

int	ft_nbh(unsigned int n)
{
	char		*s;
	char		res[9];
	int			i;

	i = 0;
	s = "0123456789abcdef";
	while (n >= 16)
	{
		res[i] = s[n % 16];
		n = n / 16;
		i++;
	}
	res[i] = s[n % 16];
	i++;
	res[i] = '\0';
	return (ft_putstr_mod(res));
}
